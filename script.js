function getBookCenter(el) {
  const fieldRect = bookField.getBoundingClientRect();
  const rect = el.getBoundingClientRect();

  return {
    x: rect.left - fieldRect.left + rect.width / 2,
    y: rect.top - fieldRect.top + rect.height / 2
  };
}

let currentConnectionAuthor = null;
let currentConnectionPairs = [];

function showAuthorConnections(author) {
  if (!connectionLayer) return;

  connectionLayer.innerHTML = "";

  const sameAuthorBooks = bookElements.filter(el => {
    return el.dataset.author === author;
  });

  bookElements.forEach(el => {
    el.classList.remove("author-active");
  });

  sameAuthorBooks.forEach(el => {
    el.classList.add("author-active");
  });

  if (sameAuthorBooks.length < 2) return;

  if (currentConnectionAuthor !== author || currentConnectionPairs.length === 0) {
    currentConnectionAuthor = author;
    currentConnectionPairs = createRandomAuthorNetwork(sameAuthorBooks);
  }

  currentConnectionPairs.forEach(pair => {
    const start = getBookCenter(pair[0]);
    const end = getBookCenter(pair[1]);

    const line = document.createElementNS("http://www.w3.org/2000/svg", "line");

    line.setAttribute("x1", start.x);
    line.setAttribute("y1", start.y);
    line.setAttribute("x2", end.x);
    line.setAttribute("y2", end.y);
    line.classList.add("connection-line");

    connectionLayer.appendChild(line);
  });
}

function createRandomAuthorNetwork(authorBooks) {
  const pairs = [];
  const connected = new Set();

  // 1단계: 전체 책이 끊기지 않게 최소 연결 만들기
  for (let i = 0; i < authorBooks.length - 1; i++) {
    pairs.push([authorBooks[i], authorBooks[i + 1]]);
    connected.add(authorBooks[i]);
    connected.add(authorBooks[i + 1]);
  }

  // 2단계: 책 하나당 선 개수를 1~5개 사이로 보강
  authorBooks.forEach(book => {
    const currentCount = pairs.filter(pair => {
      return pair[0] === book || pair[1] === book;
    }).length;

    const targetCount = Math.floor(Math.random() * 5) + 1;
    const needed = Math.max(0, targetCount - currentCount);

    const candidates = authorBooks.filter(otherBook => {
      return otherBook !== book && !hasPair(pairs, book, otherBook);
    });

    shuffleArray(candidates);

    for (let i = 0; i < needed && i < candidates.length; i++) {
      pairs.push([book, candidates[i]]);
    }
  });

  return pairs;
}

function hasPair(pairs, a, b) {
  return pairs.some(pair => {
    return (
      (pair[0] === a && pair[1] === b) ||
      (pair[0] === b && pair[1] === a)
    );
  });
}

function shuffleArray(array) {
  array.sort(() => Math.random() - 0.5);
}

function clearAuthorConnections() {
  if (!connectionLayer) return;

  connectionLayer.innerHTML = "";

  bookElements.forEach(el => {
    el.classList.remove("author-active");
  });

  currentConnectionAuthor = null;
  currentConnectionPairs = [];
}

function clearAuthorConnections() {
  connectionLayer.innerHTML = "";

  bookElements.forEach(el => {
    el.classList.remove("author-active");
  });
}

let activeAuthor = null;

function animateConnections() {
  const hovered = bookElements.find(el => el.matches(":hover"));

  if (hovered) {
    activeAuthor = hovered.dataset.author;
    showAuthorConnections(activeAuthor);
  }

  requestAnimationFrame(animateConnections);
}

