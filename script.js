function createFloatingBooks() {
  const shuffledBooks = [...books].sort(() => Math.random() - 0.5);

  shuffledBooks.forEach(book => {
    const img = document.createElement("img");

    img.src = book.image;
    img.alt = book.title;
    img.className = "book";

    img.dataset.title = book.title;
    img.dataset.author = book.author;
    img.dataset.link = book.link;

    img.style.left = Math.random() * 88 + 4 + "vw";
    img.style.top = Math.random() * 78 + 8 + "vh";
    img.style.animationDelay = Math.random() * 8 + "s";
    img.style.animationDuration = 10 + Math.random() * 12 + "s";

    const moveX = Math.random() * 140 - 70;
    const moveY = Math.random() * 140 - 70;
    const startRotate = Math.random() * 16 - 8;
    const endRotate = Math.random() * 24 - 12;

    img.style.setProperty("--move-x", `${moveX}px`);
    img.style.setProperty("--move-y", `${moveY}px`);
    img.style.setProperty("--start-rotate", `${startRotate}deg`);
    img.style.setProperty("--end-rotate", `${endRotate}deg`);

    img.addEventListener("click", () => {
      window.location.href = book.link;
    });

    img.addEventListener("mouseenter", () => {
      showAuthorConnections(book.author);
    });

    img.addEventListener("mouseleave", () => {
      clearAuthorConnections();
    });

    bookField.appendChild(img);
    bookElements.push(img);
  });
}



function openSearch() {
  searchBox.classList.add("active");
  overlay.classList.add("active");
  results.classList.add("active");
  searchInput.focus();
  renderResults("");
}

function closeSearch() {
  searchBox.classList.remove("active");
  overlay.classList.remove("active");
  results.classList.remove("active");
  searchInput.value = "";
}

function renderResults(query) {
  const lowerQuery = query.toLowerCase();

  const filtered = books.filter(book =>
    book.title.toLowerCase().includes(lowerQuery) ||
    book.author.toLowerCase().includes(lowerQuery)
  );

  results.innerHTML = "";

  filtered.forEach(book => {
    const item = document.createElement("div");
    item.className = "result-item";

    item.innerHTML = `
      <div class="result-title">${book.title}</div>
      <div class="result-author">${book.author}</div>
    `;

    item.addEventListener("click", () => {
      window.location.href = book.link;
    });

    results.appendChild(item);
  });

  if (filtered.length === 0) {
    results.innerHTML = `<div class="result-item">No results found.</div>`;
  }
}

function renderAuthors() {
  const authors = [...new Set(books.map(book => book.author))].sort();

  authorsList.innerHTML = "";

  authors.forEach(author => {
    const item = document.createElement("div");
    item.className = "author-item";
    item.textContent = author;

    item.addEventListener("click", () => {
      const firstBook = books.find(book => book.author === author);
      window.location.href = firstBook.link;
    });

    authorsList.appendChild(item);
  });
}

